func autocomplete(teams []struct{ name, id string }, typed string) []*discordgo.ApplicationCommandOptionChoice {
	typed = strings.ToLower(typed)
	var choices []*discordgo.ApplicationCommandOptionChoice
	for _, t := range teams {
		if typed == "" || strings.Contains(strings.ToLower(t.name), typed) {
			choices = append(choices, &discordgo.ApplicationCommandOptionChoice{Name: t.name, Value: t.id})
			if len(choices) == 25 {
				break
			}
		}
	}
	return choices
}

func NBAAutocomplete(_ *dcmd.Data, arg *dcmd.ParsedArg) ([]*discordgo.ApplicationCommandOptionChoice, error) {
	return autocomplete(nbaTeams, arg.Str()), nil
}

func NFLAutocomplete(_ *dcmd.Data, arg *dcmd.ParsedArg) ([]*discordgo.ApplicationCommandOptionChoice, error) {
	return autocomplete(nflTeams, arg.Str()), nil
}

func CollegeAutocomplete(_ *dcmd.Data, arg *dcmd.ParsedArg) ([]*discordgo.ApplicationCommandOptionChoice, error) {
	return autocomplete(collegeTeams, arg.Str()), nil
}
